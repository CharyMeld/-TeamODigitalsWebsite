<script setup>
import { Head, Link, useForm, usePage } from '@inertiajs/vue3';
import { onMounted, ref, computed } from 'vue';
import AuthenticationCard from '@/Components/AuthenticationCard.vue';
import AuthenticationCardLogo from '@/Components/AuthenticationCardLogo.vue';
import Checkbox from '@/Components/Checkbox.vue';
import InputError from '@/Components/InputError.vue';
import InputLabel from '@/Components/InputLabel.vue';
import PrimaryButton from '@/Components/PrimaryButton.vue';
import TextInput from '@/Components/TextInput.vue';

defineProps({
    canResetPassword: Boolean,
    status: String,
});

// Login form state
const form = useForm({
    email: '',
    password: '',
    remember: false,
});

const emailInput = ref(null);
const showPassword = ref(false);

// Get login attempts remaining from session
const page = usePage();
const attemptsRemaining = computed(() => page.props.flash?.login_attempts_remaining);

// Toggle password visibility
const togglePasswordVisibility = () => {
    showPassword.value = !showPassword.value;
};

// Submit login form - Simplified for Inertia
const submit = () => {
    form.transform(data => ({
        ...data,
        remember: form.remember ? 'on' : '',
    })).post(route('login'), {
        onFinish: () => {
            // Clear password after submission (success or error)
            form.reset('password');
        },
        onSuccess: () => {
            // Inertia automatically handles redirect to dashboard
            console.log('Login successful');
        },
        onError: (errors) => {
            console.error('Login errors:', errors);
        }
    });
};

// Autofocus email input
onMounted(() => {
    if (emailInput.value) {
        emailInput.value.focus();
    }
});
